    int n;
    cin >> n;
    for(int i=1;i<=n;i++){
        ndp[3] = dp[1] + dp[2] + 10*dp[3];
        ndp[2] = 9*dp[2] + dp[0];
        ndp[1] = 9*dp[1] + dp[0];
        ndp[0] = 8*dp[0];
        for(int j=0;j<=3;j++) dp[j] = ndp[j];
    }
