int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("in", "r", stdin); //test input
    int n, m, k;
    cin >> n >> m >> k;
    n--;
    int sz = 1;
    while(sz <= n) sz *= 2;
    vector<Mint> poly(sz, 0);   
    poly[0] = m-1;
    poly[1] = 1;
    ntt(poly, sz, 1);
    vector<Mint> ans(sz, 0);
    ans[0] = m;
    ntt(ans, sz, 1);
    while(n){
        if(n & 1){
            for(int j=0;j<sz;j++){
                ans[j] = ans[j] * poly[j];
            }
        }
        n >>= 1;
        for(int j=0;j<sz;j++) poly[j] *= poly[j];
    }
    ntt(ans, sz, -1);
    
    for (int i = 0; i < sz; i++) ans[i] /= sz;
    Mint anss = 0;
    for(int i=0;i<=k;i++) anss += ans[i];
    cout << anss << "\n";
}
