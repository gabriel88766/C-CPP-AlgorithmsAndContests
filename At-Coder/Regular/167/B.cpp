int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("in", "r", stdin); //test input
    ll a, b;
    cin >> a >> b;
    // product of divisors of a^b 
    vector<ll> pcnt;
    auto aux = a;
    bool sq = true;
    for(ll i = 2; i*i <= aux; i++){
        int cnt = 0;
        while(!(aux % i)){
            aux /= i;
            cnt++;
        }
        if(cnt != 0) pcnt.push_back(cnt);
    }
    if(aux != 1){
         pcnt.push_back(1);
    }
    Mint ans = 1;
    for(auto x: pcnt){
         if(x % 2) sq = false;
         ans *= (Mint(x) * Mint(b) + 1);
    }
    ans *= b;
    if(!(b % 2)) sq = false;
    if(sq) ans -= 1;
    ans /= 2;
    cout << ans << "\n";
    


}
