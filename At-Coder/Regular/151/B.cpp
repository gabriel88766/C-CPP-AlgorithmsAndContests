int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("in", "r", stdin); //test input
    //consider a1 != ap1, then a1 = ap1, then a1 = ap1 and a2 = ap2, then...
    init();
    int n, m;
    cin >> n >> m;
    vector<int> p(n+1);
    for(int i=1;i<=n;i++) cin >> p[i];
    if(m == 1){
        cout << "0\n";
        return 0;
    }
    Mint lex = Mint(m) * Mint(m-1)/2;//m-1 + m-2 + m-3 + ...
    int cmp = n;
    Mint ans = 0;
    for(int i=1;i<=n;i++){
        if(get(i) != get(p[i])){
            //then it is possible
            ans += Mint(m).pow(cmp - 2) * lex;
            unite(i, p[i]);
            cmp--;
        }
    }
    cout << ans << "\n";
}
