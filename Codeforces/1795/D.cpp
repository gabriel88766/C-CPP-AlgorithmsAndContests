#include <bits/stdc++.h>
typedef long long int ll;
typedef unsigned long long int ull;
const ll INF_LL = 0x3f3f3f3f3f3f3f3f, MOD = 998244353;
const int INF_INT = 0x3f3f3f3f;
const long double PI = acosl(-1.), EPS = 1e-9; 
using namespace std;

const int N = 3e5+2; //O(N) preprocessing, O(1) query

ll fat[N], invfat[N];
void init(){ //MOD must be prime
    fat[0] = invfat[N-1] = 1;
    for(int i=1;i<N;i++){
        fat[i] = (fat[i-1]*i) % MOD;
    }
    ll aux = MOD - 2, aux2 = fat[N-1];
    while(aux){
        if(aux & 1) invfat[N-1] = (invfat[N-1] * aux2) % MOD;
        aux2 = (aux2 * aux2) % MOD;
        aux >>= 1;
    }
    for(int i=N-2;i>=0;i--) invfat[i] = (invfat[i+1] * (i + 1)) % MOD;
}
ll nCr(ll a, ll b){
    return (((fat[a] * invfat[a-b]) % MOD) * invfat[b]) % MOD;
}

//cout << fixed << setprecision(6)
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("in", "r", stdin); //test input
    init();
    int n;
    cin >> n;
    ll ans = 1;
    for(int i=1;i<=(n/3);i++){
        int a, b, c;
        cin >> a >> b >> c;
        int pos[] = {a+b, a+c, b+c};
        sort(pos, pos+3);
        ll cnt;
        if(pos[2] == pos[1]){
            if(pos[1] == pos[0]) cnt = 3;
            else cnt = 2;
        }else cnt = 1;
        ans = (ans * cnt) % MOD;
    }
    ans = (ans * nCr(n/3, n/6)) % MOD;
    cout << ans;
}
