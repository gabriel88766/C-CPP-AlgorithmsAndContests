//cout << fixed << setprecision(6)
int main(){
    ios_base::sync_with_stdio(false);
    cin.tie(NULL);
    //freopen("in", "r", stdin); test input
    int t;
    cin >> t;
    while(t--){
        int n, ans = 1;//everybody goes
        cin >> n;
        vector<int> v(n);
        for(int i=0;i<n;i++) cin >> v[i];
        sort(v.begin(), v.end());
        if(v[0]) ans++; //nobody goes
        for(int i=0;i<(n-1);i++){
            if(v[i+1] > (i+1) && v[i] <= i) ans++;
        }
        cout << ans << "\n";
    }
}
